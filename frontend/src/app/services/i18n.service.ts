@Injectable({
  providedIn: 'root'
})
export class I18nService {
  private currentLang = new BehaviorSubject<Language>('en');
  public lang$ = this.currentLang.asObservable();

  constructor() {
    // Restore language from localStorage
    const savedLang = localStorage.getItem('language') as Language;
    if (savedLang && (savedLang === 'en' || savedLang === 'fr')) {
      this.setLanguage(savedLang);
    } else {
      // Auto-detect browser language
      const browserLang = navigator.language.split('-')[0];
      if (browserLang === 'fr') {
        this.setLanguage('fr');
      }
    }
  }

  getLanguage(): Language {
    return this.currentLang.value;
  }

  setLanguage(lang: Language): void {
    this.currentLang.next(lang);
    localStorage.setItem('language', lang);
    document.documentElement.setAttribute('lang', lang);
  }

  /**
   * Translate a key (e.g., 'dashboard.title')
   * Supports interpolation with {key} syntax
   */
  t(key: string, params?: Record<string, string | number>): string {
    const keys = key.split('.');
    let result: any = TRANSLATIONS[this.currentLang.value];

    for (const k of keys) {
      if (result && typeof result === 'object' && k in result) {
        result = result[k];
      } else {
        // Fallback to English
        result = TRANSLATIONS['en'];
        for (const fallbackKey of keys) {
          if (result && typeof result === 'object' && fallbackKey in result) {
            result = result[fallbackKey];
          } else {
            return key; // Return key if not found
          }
        }
        break;
      }
    }

    if (typeof result === 'string') {
      // Handle interpolation
      if (params) {
        return result.replace(/\{(\w+)\}/g, (_, paramKey) => {
          return params[paramKey]?.toString() ?? `{${paramKey}}`;
        });
      }
      return result;
    }

    return key;
  }

  /**
   * Get all translations for a section
   */
  getSection(section: string): Translations {
    const translations = TRANSLATIONS[this.currentLang.value];
    return (translations[section] as Translations) || {};
  }

  /**
   * Check if a translation key exists
   */
  has(key: string): boolean {
    const keys = key.split('.');
    let result: any = TRANSLATIONS[this.currentLang.value];

    for (const k of keys) {
      if (result && typeof result === 'object' && k in result) {
        result = result[k];
      } else {
        return false;
      }
    }

    return typeof result === 'string';
  }

  /**
   * Get available languages
   */
  getAvailableLanguages(): { code: Language; name: string }[] {
    return [
      { code: 'en', name: 'English' },
      { code: 'fr', name: 'Franais' }
    ];
  }
}
